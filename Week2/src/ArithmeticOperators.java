public class ArithmeticOperators {

    public static void main(String[] args) {


        int x = 5;
        int y = 2;

        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));

        // Modulus
        System.out.println("x % y = " + (x % y));

        // Increment - Decrement
        System.out.println("y++ = " + y++);
        System.out.println("x-- = " + x--);
        System.out.println("--y = " + --y);
        System.out.println("--x = " + --x);




    }
}
