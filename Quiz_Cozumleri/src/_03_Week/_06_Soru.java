package _03_Week;

public class _06_Soru {

    /*

        Eğer m = −12 ve n = −5, ise m%n işleminin sonucu ne olur?

        Cevap = "-2"

     */

    public static void main(String[] args) {

        // 5 * 1 = 5, 5 * 2 = 10, ---> -12 - 10 = -2 kalan

        int m = -12;
        int n = -5;

        System.out.println(m % n);

    }
}
