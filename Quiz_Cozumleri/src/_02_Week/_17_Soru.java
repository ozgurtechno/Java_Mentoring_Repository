package _02_Week;

public class _17_Soru {

    /*

        Escape karakterlerini yaptıkları işleve göre eşleştirin.

     */

    public static void main(String[] args) {

        System.out.println("1. Çıktı\nVe 1. Çıktının devamı"); // \n anahtarı çıktıya
        // bir satır aşşığdan devam eder.

        System.out.println("2. Çıktı\\Buraya bir Slash koyuyorum!"); // \\ anahtari bir slash birakir.

        System.out.println("3. Çıktı\tVe 3. Çıktının devamı!"); // \t anahtarı bir tab
        // boşluk birakarak çıktıya devam eder

        System.out.println("4. Çıktı\b"); // \b anahtari bir karakter geriye siler

        System.out.println("5. Çıktının İçerisinden \"Merhaba\""); // \" Tek tırnak koyar

        System.out.println("6. Çıktı\r6. Çıktının Satır Başından Devam Ediyorum!"); // \r Satır başına kadar siler


    }
}
