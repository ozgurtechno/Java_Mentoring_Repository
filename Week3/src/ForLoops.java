public class ForLoops {

    public static void main(String[] args) {

        //For Loop
        for (int i = 0; i < 10; i++) {
            System.out.println("Merhaba Dunya");
        }


        // Nested For Loops
        for (int row = 1; row <= 4; row++) {

            for (int col = 1; col <= 4 ; col++) {
                System.out.print("*");
            }

            System.out.println();
        }


        //------------------- Ornekler ----------------------------------------------
    }
}
