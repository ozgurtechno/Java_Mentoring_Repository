package Week1_Algo;

public class Swap2Numbers {

    public static void main(String[] args) {

        int x = 6;
        int y = 12;

        // Ogrenciler bu kismi yazacak
        int temp = x;
        x = y;
        y = temp;

        // Bu kisim hazir verilecek
        System.out.println("X in yeni degeri...: " + x);
        System.out.println("Y nin yeni degeri..: " + y);

    }
}
